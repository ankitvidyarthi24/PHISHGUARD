// Generate 14-day time series relative to today so charts always show current data
function generateTimeSeries() {
  const scanPattern = [14, 18, 22, 31, 27, 19, 15, 24, 33, 41, 38, 45, 52, 20];
  const today = new Date();
  return Array.from({ length: 14 }, (_, i) => {
    const d = new Date(today);
    d.setDate(d.getDate() - (13 - i));
    const label = d.toLocaleDateString("en-US", { month: "short", day: "numeric" });
    const total = scanPattern[i];
    const phishing = Math.round(total * 0.43);
    const suspicious = Math.round(total * 0.14);
    const safe = total - phishing - suspicious;
    return { date: label, scans: total, phishing, safe, suspicious };
  });
}
