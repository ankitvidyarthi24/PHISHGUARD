// Returns an ISO timestamp N days in the past at the given hour:min
function ts(daysAgo, hour = 12, min = 0) {
  const d = new Date();
  d.setDate(d.getDate() - daysAgo);
  d.setHours(hour, min, 0, 0);
  return d.toISOString();
}

